// Build the classroom screen kit's stylesheet from the Claude Design canvas.
//
//   node scripts/build-screen-kit.mjs
//
// Input:  Claude Design Wireframe/Lesson Frame Wireframes.dc.html  (turn 12)
//         Claude Design Wireframe/_ds/<system>/tokens/*.css
// Output: public/screens/_system/frame.css   GENERATED - do not hand-edit
//
// Ownership contract (the whole point of the kit):
//   - _system/frame.css is GENERATED. Re-run this script when Design ships a
//     new canonical turn. Never hand-edit it.
//   - public/screens/*.html are OWNED BY HAND. No script writes them after
//     their first scaffold. They are the surface Steele edits directly.
//
// The stylesheet is the turn-12 subset of the canvas: design tokens, then
// every canvas rule whose selector uses a class that turn 12's markup uses.
// Filtering keeps the kit discoverable - a hand-editor sees only classes
// that actually exist on the screens.

import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const SRC = join(root, "Claude Design Wireframe", "Lesson Frame Wireframes.dc.html");
const OUT_DIR = join(root, "public", "screens", "_system");
const TOKEN_ORDER = ["fonts.css", "colors.css", "typography.css", "spacing.css"];
// Canvas presentation chrome, not screen components - never part of the kit.
const CANVAS_CHROME = /^(dv-|mnote$|rolelbl$|frame$)/;

const html = readFileSync(SRC, "utf8");

function turnMarkup(id) {
  const start = html.indexOf(`<section class="dv-turn" id="${id}">`);
  if (start === -1) throw new Error(`Turn ${id} not found`);
  const next = html.indexOf('<section class="dv-turn"', start + 1);
  return html.slice(start, next === -1 ? html.length : next);
}

function usedClasses(markup) {
  const set = new Set();
  for (const m of markup.matchAll(/class="([^"]+)"/g)) {
    for (const c of m[1].split(/\s+/)) {
      if (c && !CANVAS_CHROME.test(c)) set.add(c);
    }
  }
  return set;
}

function tokens() {
  const dsRoot = join(root, "Claude Design Wireframe", "_ds");
  const system = readdirSync(dsRoot).find((d) => d.startsWith("big-dog-board-design-system"));
  const dir = join(dsRoot, system, "tokens");
  return TOKEN_ORDER.filter((f) => existsSync(join(dir, f)))
    .map((f) => `/* ---- tokens/${f} ---- */\n${readFileSync(join(dir, f), "utf8")}`)
    .join("\n");
}

// Keep a canvas rule-line when any class selector on it is in the used set.
function filteredCanvasCss(used) {
  const css = html.match(/<style>([\s\S]*?)<\/style>/)[1];
  const kept = [];
  for (const line of css.split("\n")) {
    const classes = [...line.matchAll(/\.([A-Za-z][\w-]*)/g)].map((m) => m[1]);
    if (classes.length && classes.some((c) => used.has(c))) kept.push(line);
  }
  return kept.join("\n");
}

const used = usedClasses(turnMarkup("t12"));

const css = `/* GENERATED by scripts/build-screen-kit.mjs - DO NOT HAND-EDIT.
   Turn-12 subset of the Claude Design "Lesson Frame Wireframes" canvas:
   the Warm Notebook system for all four classroom surfaces.
   Screens in public/screens/*.html are the hand-owned layer on top. */

/* Handwritten voice - teacher asides ONLY, never student-required text. */
@import url('https://fonts.googleapis.com/css2?family=Caveat:wght@500;600;700&display=swap');

${tokens()}

/* ---- turn-12 canvas rules, filtered to classes the screens use ---- */
${filteredCanvasCss(used)}

/* ---- kit base (not from the canvas) ---- */
html, body { margin: 0; height: 100%; background: #E7E2D8; overflow: hidden;
  font-family: var(--font-body); }
/* Screens are authored on a fixed 1440x810 stage; frame.js scales it to the
   viewport, so hand-edits stay in simple pixels. */
.vmstage { width: 1440px; height: 810px; }
`;

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(join(OUT_DIR, "frame.css"), css);
const ruleCount = (css.match(/\n\./g) || []).length;
console.log(`frame.css: ${used.size} screen classes, ~${ruleCount} rule lines, ${(css.length / 1024).toFixed(0)} KB`);
